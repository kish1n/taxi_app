class Car:
    def __init__(self, make, model, year, color, car_number, num_seats, trunk_space, car_type):

        self.make = make
        self.model = model
        self.year = year
        self.color = color
        self.car_number = car_number
        self.num_seats = num_seats
        self.trunk_space = trunk_space
        self.car_type = car_type
